#include<iostream>
using namespace std;

int main() {

    // Integer
    int a = 123;
    cout << a << endl;

    // Character
    char b = 'v';
    cout << b << endl;

    // Bool
    bool bl = true;
    cout << bl << endl;

    // float
    float  f = 1.2;
    cout << f << endl;

    // double
    double d = 1.23;
    cout << d << endl;

    // sizeof
    int size = sizeof(d);
    cout << "size of a is:- " << size << endl;

}